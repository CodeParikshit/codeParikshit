import java.util.*;
class Naturalnumber_Reverse{
public static void main(String args[]){
int N;
Scanner sc = new Scanner (System.in);
System.out.println("enter the n natural no");
N = sc.nextInt();
int i,j;
System.out.println("all natural no. in reverse are");
for (i=N;i>=1;i--){
System.out.println(i);
}
System.out.println("all natural no in are");
for (j=1;j<=N;j++){
System.out.print(j);

}
}
}




